public class petle_tmp {
    public static void main(String[] args) {
        // FOR

        //przyklad1
        for (int i=0; i<10; i++){                               // i niedostepne poza petla
            System.out.println("1. current value " + i);
        }

        //przyklad2
        int i = 0;
        for (; i<10; i++){
            System.out.println("2. current value " + i);
        }

        //przyklad3
        int j = 0;
        for (; j<10;){
            System.out.println("3. current value " + j);
            j++;
        }

        // WHILE
        //przyklad4

        int k = 0;
        while(k<10){
            System.out.println();
        }



        // DO WHILE
    }
}

